package OpretorsInJava;

public class S005_arithmaticOpretor {

public static void main(String[] args) {
		
		
		int a = 12;
		int b = 23;
		
		System.out.println("ADDITION IS : "+(a+b));
		
		
		int c = 77;
		int d = 23;
		
		System.out.println("MULTIPLICATION IS : "+(c*d));
		
		
		int e = 999;
		int f = 888;
		
		System.out.println("SUBSTRACTION IS : "+(e-f));
		
		
		int g = 102;
		int h = 10;
		
		System.out.println("DIVISION IS : "+((float) g/h));
		
		
		
		int i  = 77;
		int j =  22;
		
		System.out.println("MODULAR VALUE IS : "+(i%j));
	}
}
