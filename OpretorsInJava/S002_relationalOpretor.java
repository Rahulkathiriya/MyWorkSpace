package OpretorsInJava;

public class S002_relationalOpretor {
	
	
public static void main(String[] args) {
	
		
		int a = 22;
		int b = 21;
		
		System.out.println("CONDITION IS : "+(a<b));
		
		int c = 22;
		int d = 21;
		
		System.out.println("CONDITION IS : "+(c>d));
		
		int e = 22;
		int f = 21;
		
		System.out.println("CONDITION IS : "+(e<=f));
		
		int g = 21;
		int h = 21;
		
		System.out.println("CONDITION IS : "+(g>=h));
		System.out.println("CONDITION IS : "+(g==h));
		System.out.println("CONDITION IS : "+(g!=h));

	}



}
